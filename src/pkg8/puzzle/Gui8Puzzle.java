    private void showRestart(){
        int pil = JOptionPane.showConfirmDialog(this, "Ingin Mengulang Permainan?", "Reload Game?", JOptionPane.YES_NO_OPTION);
        if(pil == JOptionPane.YES_OPTION){
            dispose();
            Gui8Puzzle gp = new Gui8Puzzle();
            gp.looknfeel();
            gp.setVisible(true);
        } else if(pil == JOptionPane.NO_OPTION) {
            JOptionPane.showMessageDialog(this, "Terima kasih telah bermain");
            dispose();
        }
    }
    
