    public static String[][] shufflearray(String[][] array){
        Random r = new Random();
        
        for (int i = 0; i < array.length; i++){
            for(int j = 0; j < array[i].length; j++){
                int rndm2 = r.nextInt(array[i].length);
                int rndm1 = r.nextInt(array.length);
                String tmp = array[i][j];
                array[i][j] = array[rndm1][rndm2];
                array[rndm1][rndm2] = tmp;
            }
        }
        return array;
    }
    
